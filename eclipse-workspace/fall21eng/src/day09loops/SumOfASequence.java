package day09loops;

public class SumOfASequence {

	public static void main(String[] args) {
	int sum=0;	
	for(int i=12 ;i<15; i++) {
	sum=sum+i;
	}
	System.out.print(""+ sum);
	
	int multiplication=1;
	
	for(int i=3;i<6;i++) {
		multiplication=multiplication*i;
		System.out.println(multiplication);
	}
	}

}
