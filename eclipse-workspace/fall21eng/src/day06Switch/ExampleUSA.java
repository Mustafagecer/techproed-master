package day06Switch;

public class ExampleUSA {

	public static void main(String[] args) {
		char FirstLetter='K';
		
		switch(FirstLetter) {
		case 'U': System.out.println("United");
		break;
		
		case 'S': System.out.println("State");
		break;
		
		case 'A': System.out.println("America");
		
		default: System.out.println("Unvalid Letter");
}
	}

}
